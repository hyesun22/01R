
public class Ex03_boolean
{

	public static void main(String[] args)
	{
		boolean check1 = true;
		boolean check2 = false;   //직접 값 대입
		boolean check3 = (1 < 2); //연산의 결과를 값으로 대입해주겠다.
		
		System.out.println(check1);
		System.out.println(check2);
		System.out.println(check3); // true
		System.out.println(1 > 2); //연산 결과값 출력(false)

	}

}
